class Solution(object):
    def permute(self, nums):
        """
        :type nums: List[int]
        :rtype: List[List[int]]
        """
        result = []
        def _permute(nums, p):
            for num in nums:
                if num in p:
                    continue

                p.append(num)
                if len(nums) == len(p):
                    result.append(p)
                    return

                _permute(nums, list(p))
                p.pop()

        if nums:
            _permute(nums, [])

        return result
#======================================================================================================================= Functions

