class Solution(object):
    def containsDuplicate(self, nums):
        """
        :type nums: List[int]
        :rtype: bool
        """
        counts = dict()
        if len(nums) > 0:
            for i in nums:
                if i in counts:
                    return True
                counts[i] = 1

        return False
